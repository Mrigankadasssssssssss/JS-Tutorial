// Loose equaity(==) and Strict Equality(===)

const age = 21

console.log(age=='21') // Focus on value not the type -> This is loose equality(==)  // true

console.log(age==='21') // Focus o both the value and as well as type -> This is strict equality(===) // false

console.log(age!='21') //false

console.log(age!=='21') // true

